const add = (num, num2) => num + num2;

const subtract = (num, num2) => num - num2;

const multiply = (num, num2) => num * num2;

const divide = (num, num2) => num / num2;

const sqrt = (num, num2) => Math.sqrt(num);

const max = (num, num2) => Math.max(num, num2);

module.exports={
    add,
    subtract,
    multiply,
    divide,
    sqrt,
    max
}